public class DefineBasicInfo {
    public static void main(String[] args) {
        // Define several things as a variable then print their values
        String name = "Archie";// Your name as a string
        System.out.println(name);

        int age = 30;// Your age as an integer
        System.out.println(age);

        double height = 1.8;// Your height in meters as a double
        System.out.println(height);

        boolean married = false;// Whether you are married or not as a boolean
        System.out.println("Married: " + married);
    }
}